package controlflow;

public class ForLoop {
    public static void main(String[] args) {
        //for loop(pre test loop)
        // for (initialization, condition, increasement/decreasement){
        // code to repeat
        // }


        for (int i=0; i<10; i++){
            System.out.println("Number is :" +i);
        }

        for (int j=10; j>1; j--){
            System.out.println(j+ "\t");
        }
    }
}
